export function saveMockRequest(req: Request): void {
  const idx = _requests.findIndex(r => r.id === req.id)
  if (idx >= 0) {
    _requests[idx] = req
  } else {
    _requests = [req, ..._requests]
  }
}

export function deleteMockRequest(id: string): void {
  _requests = _requests.filter(r => r.id !== id)
}

